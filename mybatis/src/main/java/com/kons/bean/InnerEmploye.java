package com.kons.bean;

public class InnerEmploye {
    private int ie_id;
    private String ie_code;
    private String ie_department;
    private String ie_phone;
    private String ie_job;

    public int getIe_id() {
        return ie_id;
    }

    public void setIe_id(int ie_id) {
        this.ie_id = ie_id;
    }

    public String getIe_code() {
        return ie_code;
    }

    public void setIe_code(String ie_code) {
        this.ie_code = ie_code;
    }

    public String getIe_department() {
        return ie_department;
    }

    public void setIe_department(String ie_department) {
        this.ie_department = ie_department;
    }

    public String getIe_phone() {
        return ie_phone;
    }

    public void setIe_phone(String ie_phone) {
        this.ie_phone = ie_phone;
    }

    public String getIe_job() {
        return ie_job;
    }

    public void setIe_job(String ie_job) {
        this.ie_job = ie_job;
    }
}
