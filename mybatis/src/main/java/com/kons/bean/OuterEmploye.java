package com.kons.bean;

public class OuterEmploye {
    private int id;
    private String op_code;
    private String op_company;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getOp_code() {
        return op_code;
    }

    public void setOp_code(String op_code) {
        this.op_code = op_code;
    }

    public String getOp_company() {
        return op_company;
    }

    public void setOp_company(String op_company) {
        this.op_company = op_company;
    }
}
